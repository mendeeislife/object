#include <iostream>
using namespace std;

// Pi тоог зарлаж өгч байна (дараа нь ашиглахад хялбар)
#define pi 3.14159265358979323846

// Тойргийн радиусыг гараас авч талбайг бодож хэвэл.
int main() {
	// Гараас авах радиусыг хадгалах болон олсон талбайг хадгалах хувьсагч зарлах
	int r;
	double s;

	// Гараас радиус авах
	cin >> r;

	// pi*r^2 томъёо-г ашиглан талбайг олж байна
	s = pi * (r * r);
	
	// Олсон талбайг хэвлэх
	cout << "Area: " << s << endl;
	
	return 0;
}
